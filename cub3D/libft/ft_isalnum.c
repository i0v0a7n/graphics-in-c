#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c <= '9' && c >= '0') || (c <= 'Z' && c >= 'A') \
	|| (c <= 'z' && c >= 'a'))
		return (1);
	else
		return (0);
}
